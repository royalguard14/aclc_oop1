package Simulation;
// displaying output in the console
// working with comments

public class Java01 {

    public static void main(String[] args) {

        //displaying output with print() and println()
        System.out.print("I am learning Java");
        System.out.print(" and it is fun.\n");  //adding new line with \n 
        System.out.print("You should try it!");

        System.out.println();   //adding new line into the display
        System.out.println("Hello World!");

        //adding escape sequence to display the double quotes
        System.out.print("\n");
        System.out.println("\"JAVA Programming\"");

        //adding tab with escape sequence
        System.out.println("\tJAVA Programming");

        //this is a single line comment
        /*
        this is a 
        multi-line comment
         */
    }
}
